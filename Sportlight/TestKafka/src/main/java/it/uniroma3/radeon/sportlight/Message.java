package it.uniroma3.radeon.sportlight;

public class Message {
	private String type;
	private String t;
	private String k;
	
	public Message() {}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getT() {
		return t;
	}

	public void setT(String t) {
		this.t = t;
	}

	public String getK() {
		return k;
	}

	public void setK(String k) {
		this.k = k;
	}
	
}
